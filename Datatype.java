public class Datatype {
    public static void main(String[] args) {
        
        float var = 3;
        System.out.println("float=" + var);
        double var2 = 4.45;
        System.out.println("double=" + var2);
        char var3 = 'h'; 
        System.out.println("char=" + var3);
        boolean var4 = true;
        System.out.println("boolean= " + var4);
        short var5 = 3000;
        System.out.println("short=" + var5);
        long var6 = 1000000000;
        System.out.println("long =" + var6);
        String var7 = "Hi guys how are u ?";
        System.out.println("string=" + var7); 

    }
}
